#ifndef ARCHQOR_X86PC_TRAMPOLINEWRITER_H_2
#define ARCHQOR_X86PC_TRAMPOLINEWRITER_H_2

#include "CompilerQOR.h"
#include "ArchQOR/x86/Assembler/Defs.h"

//------------------------------------------------------------------------------
namespace nsArch
{
//------------------------------------------------------------------------------
namespace nsx86
{

#if ( QOR_ARCH_WORDSIZE == 64 )

	//------------------------------------------------------------------------------
	//Class used to determine size of trampoline and as trampoline writer.
	struct __QCMP_LOCAL TrampolineWriter
	{
		//------------------------------------------------------------------------------
		// Size of trampoline
		enum 
		{
			TRAMPOLINE_JMP = 6,
			TRAMPOLINE_ADDR = sizeof( Cmp_int_ptr ),
			TRAMPOLINE_SIZE = TRAMPOLINE_JMP + TRAMPOLINE_ADDR
		};

		//------------------------------------------------------------------------------
		// Write trampoline into code at address @a code that will jump to @a target.
		static void writeTrampoline( Cmp_unsigned__int8* code, void* target )
		{			
			code[ 0 ] = 0xFF;															// Jmp.
			
			code[ 1 ] = 0x25;															// ModM (RIP addressing).
			
			( (Cmp_unsigned__int32*)( code + 2 ) )[ 0 ] = 0;							// Offset (zero).
			
			( (Cmp_uint_ptr*)( code + TRAMPOLINE_JMP ) )[ 0 ] = (Cmp_uint_ptr)target;	// Absolute address.
		}
	};

#endif

}//nsx86
}//nsArch

#endif//ARCHQOR_X86PC_TRAMPOLINEWRITER_H_2
