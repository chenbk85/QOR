//SanOS specific definitions associated with stddef

#ifndef SYSTEMQOR_SAN_TYPES_STDDEF_H_1
#define SYSTEMQOR_SAN_TYPES_STDDEF_H_1

//#include "grp_types.h" //TODO:

//--------------------------------------------------------------------------------
namespace nsSanOS
{
	typedef int             errno_t;
	typedef Cmp__int3264    ptrdiff_t;
	typedef Cmp__int3264    intptr_t;
	typedef long			off_t;

	// Define offsetof macro 
#ifdef __cplusplus
#	define offsetof(s,m)   (size_t)&reinterpret_cast<const volatile char&>((((s *)0)->m))
#else
#	define offsetof(s,m)   (size_t)&(((s *)0)->m)
#endif

#if (!__QCMP_FEATURE_PREDEFINESSIZE_T)
	typedef unsigned int size_t;
#endif

	typedef signed long ssize_t;

}//nsSanOS

#endif //SYSTEMQOR_SAN_TYPES_STDDEF_H_1
