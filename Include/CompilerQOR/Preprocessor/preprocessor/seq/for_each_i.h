#ifndef QOR_PREPROCESSOR_SEQ_FOR_EACH_I_H_1
#define QOR_PREPROCESSOR_SEQ_FOR_EACH_I_H_1

#include "../arithmetic/dec.h"
#include "../arithmetic/inc.h"
#include "../config/config.h"
#include "../repetition/for.h"
#include "seq.h"
#include "size.h"
#include "../tuple/elem.h"
#include "../tuple/rem.h"

//QOR_PP_SEQ_FOR_EACH_I

#if	~QOR_PP_CONFIG_FLAGS() & QOR_PP_CONFIG_EDG()
#	define QOR_PP_SEQ_FOR_EACH_I( macro, data, seq )		QOR_PP_FOR( ( macro, data, seq ( nil ), 0 ), QOR_PP_SEQ_FOR_EACH_I_P, QOR_PP_SEQ_FOR_EACH_I_O, QOR_PP_SEQ_FOR_EACH_I_M )
#else//EDG
#	define QOR_PP_SEQ_FOR_EACH_I( macro, data, seq )		QOR_PP_SEQ_FOR_EACH_I_I( macro, data, seq )
#	define QOR_PP_SEQ_FOR_EACH_I_I( macro, data, seq )		QOR_PP_FOR( ( macro, data, seq ( nil ), 0), QOR_PP_SEQ_FOR_EACH_I_P, QOR_PP_SEQ_FOR_EACH_I_O, QOR_PP_SEQ_FOR_EACH_I_M )
#endif

#define QOR_PP_SEQ_FOR_EACH_I_P( r, x )					QOR_PP_DEC( QOR_PP_SEQ_SIZE( QOR_PP_TUPLE_ELEM( 4, 2, x ) ) )

#if QOR_PP_CONFIG_FLAGS() & QOR_PP_CONFIG_STRICT()
#   define QOR_PP_SEQ_FOR_EACH_I_O( r, x )				QOR_PP_SEQ_FOR_EACH_I_O_I x
#else
#   define QOR_PP_SEQ_FOR_EACH_I_O( r, x )				QOR_PP_SEQ_FOR_EACH_I_O_I( QOR_PP_TUPLE_ELEM( 4, 0, x ), QOR_PP_TUPLE_ELEM( 4, 1, x ), QOR_PP_TUPLE_ELEM( 4, 2, x ), QOR_PP_TUPLE_ELEM( 4, 3, x ) )
#endif

#define QOR_PP_SEQ_FOR_EACH_I_O_I( macro, data, seq, i )	( macro, data, QOR_PP_SEQ_TAIL( seq ), QOR_PP_INC( i ) )

#if QOR_PP_CONFIG_FLAGS() & QOR_PP_CONFIG_STRICT()
#   define QOR_PP_SEQ_FOR_EACH_I_M( r, x )				QOR_PP_SEQ_FOR_EACH_I_M_IM( r, QOR_PP_TUPLE_REM_4 x )
#   define QOR_PP_SEQ_FOR_EACH_I_M_IM( r, im )			QOR_PP_SEQ_FOR_EACH_I_M_I( r, im )
#else
#   define QOR_PP_SEQ_FOR_EACH_I_M( r, x )				QOR_PP_SEQ_FOR_EACH_I_M_I( r, QOR_PP_TUPLE_ELEM( 4, 0, x ), QOR_PP_TUPLE_ELEM( 4, 1, x ), QOR_PP_TUPLE_ELEM( 4, 2, x ), QOR_PP_TUPLE_ELEM( 4, 3, x ) )
#endif

#define QOR_PP_SEQ_FOR_EACH_I_M_I( r, macro, data, seq, i )	macro( r, data, i, QOR_PP_SEQ_HEAD( seq ) )

//QOR_PP_SEQ_FOR_EACH_I_R

#if	~QOR_PP_CONFIG_FLAGS() & QOR_PP_CONFIG_EDG()
#	define QOR_PP_SEQ_FOR_EACH_I_R( r, macro, data, seq )		QOR_PP_FOR_ ## r( ( macro, data, seq ( nil ), 0 ), QOR_PP_SEQ_FOR_EACH_I_P, QOR_PP_SEQ_FOR_EACH_I_O, QOR_PP_SEQ_FOR_EACH_I_M )
#else//EDG
#    define QOR_PP_SEQ_FOR_EACH_I_R( r, macro, data, seq )		QOR_PP_SEQ_FOR_EACH_I_R_I( r, macro, data, seq )
#    define QOR_PP_SEQ_FOR_EACH_I_R_I( r, macro, data, seq )	QOR_PP_FOR_ ## r( ( macro, data, seq ( nil ), 0 ), QOR_PP_SEQ_FOR_EACH_I_P, QOR_PP_SEQ_FOR_EACH_I_O, QOR_PP_SEQ_FOR_EACH_I_M )
#endif

#endif//QOR_PREPROCESSOR_SEQ_FOR_EACH_I_H_1
