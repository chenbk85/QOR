#ifndef QOR_PREPROCESSOR_SEQ_TRANSFORM_H_1
#define QOR_PREPROCESSOR_SEQ_TRANSFORM_H_1

#include "../config/config.h"
#include "fold_left.h"
#include "seq.h"
#include "../tuple/elem.h"
#include "../tuple/rem.h"

//QOR_PP_SEQ_TRANSFORM

#if ~QOR_PP_CONFIG_FLAGS() & QOR_PP_CONFIG_EDG()
#   define QOR_PP_SEQ_TRANSFORM( op, data, seq )			QOR_PP_SEQ_TAIL( QOR_PP_TUPLE_ELEM( 3, 2, QOR_PP_SEQ_FOLD_LEFT( QOR_PP_SEQ_TRANSFORM_O, ( op, data, ( nil ) ), seq ) ) )
#else//EDG
#   define QOR_PP_SEQ_TRANSFORM( op, data, seq )			QOR_PP_SEQ_TRANSFORM_I( op, data, seq )
#   define QOR_PP_SEQ_TRANSFORM_I( op, data, seq )			QOR_PP_SEQ_TAIL( QOR_PP_TUPLE_ELEM( 3, 2, QOR_PP_SEQ_FOLD_LEFT( QOR_PP_SEQ_TRANSFORM_O, ( op, data, ( nil ) ), seq ) ) )
#endif

#if QOR_PP_CONFIG_FLAGS() & QOR_PP_CONFIG_STRICT()
#   define QOR_PP_SEQ_TRANSFORM_O( s, state, elem )			QOR_PP_SEQ_TRANSFORM_O_IM( s, QOR_PP_TUPLE_REM_3 state, elem )
#   define QOR_PP_SEQ_TRANSFORM_O_IM( s, im, elem )			QOR_PP_SEQ_TRANSFORM_O_I( s, im, elem )
#else
#   define QOR_PP_SEQ_TRANSFORM_O( s, state, elem )			QOR_PP_SEQ_TRANSFORM_O_I( s, QOR_PP_TUPLE_ELEM( 3, 0, state ), QOR_PP_TUPLE_ELEM( 3, 1, state ), QOR_PP_TUPLE_ELEM( 3, 2, state ), elem )
#endif

#define QOR_PP_SEQ_TRANSFORM_O_I( s, op, data, res, elem )	( op, data, res ( op( s, data, elem ) ) )

//QOR_PP_SEQ_TRANSFORM_S

# if ~QOR_PP_CONFIG_FLAGS() & QOR_PP_CONFIG_EDG()
#    define QOR_PP_SEQ_TRANSFORM_S( s, op, data, seq )		QOR_PP_SEQ_TAIL( QOR_PP_TUPLE_ELEM( 3, 2, QOR_PP_SEQ_FOLD_LEFT_ ## s( QOR_PP_SEQ_TRANSFORM_O, ( op, data, ( nil ) ), seq ) ) )
# else
#    define QOR_PP_SEQ_TRANSFORM_S( s, op, data, seq )		QOR_PP_SEQ_TRANSFORM_S_I( s, op, data, seq )
#    define QOR_PP_SEQ_TRANSFORM_S_I( s, op, data, seq )	QOR_PP_SEQ_TAIL( QOR_PP_TUPLE_ELEM( 3, 2, QOR_PP_SEQ_FOLD_LEFT_ ## s( QOR_PP_SEQ_TRANSFORM_O, ( op, data, ( nil ) ), seq ) ) )
# endif

#endif//QOR_PREPROCESSOR_SEQ_TRANSFORM_H_1
