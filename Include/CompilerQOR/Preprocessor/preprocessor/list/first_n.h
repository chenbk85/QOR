#ifndef QOR_PREPROCESSOR_LIST_FIRST_N_H_1
#define QOR_PREPROCESSOR_LIST_FIRST_N_H_1

#include "../arithmetic/dec.h"
#include "../config/config.h"
#include "../control/while.h"
#include "adt.h"
#include "reverse.h"
#include "../tuple/elem.h"
#include "../tuple/rem.h"

//QOR_PP_LIST_FIRST_N

#if ~QOR_PP_CONFIG_FLAGS() & QOR_PP_CONFIG_EDG()
#   define QOR_PP_LIST_FIRST_N( count, list )		QOR_PP_LIST_REVERSE( QOR_PP_TUPLE_ELEM( 3, 2, QOR_PP_WHILE( QOR_PP_LIST_FIRST_N_P, QOR_PP_LIST_FIRST_N_O, ( count, list, QOR_PP_NIL ) ) ) )
#else
#   define QOR_PP_LIST_FIRST_N( count, list	)		QOR_PP_LIST_FIRST_N_I( count, list )
#   define QOR_PP_LIST_FIRST_N_I( count, list )		QOR_PP_LIST_REVERSE( QOR_PP_TUPLE_ELEM( 3, 2, QOR_PP_WHILE( QOR_PP_LIST_FIRST_N_P, QOR_PP_LIST_FIRST_N_O, ( count, list, QOR_PP_NIL ) ) ) )
#endif

#if ~QOR_PP_CONFIG_FLAGS() & QOR_PP_CONFIG_EDG()
#   define QOR_PP_LIST_FIRST_N_P( d, data )			QOR_PP_TUPLE_ELEM( 3, 0, data )
#else
#   define QOR_PP_LIST_FIRST_N_P( d, data )			QOR_PP_LIST_FIRST_N_P_I data
#   define QOR_PP_LIST_FIRST_N_P_I( c, l, nl )		c
#endif

#if ~QOR_PP_CONFIG_FLAGS() & QOR_PP_CONFIG_MWCC()
#   define QOR_PP_LIST_FIRST_N_O( d, data )		QOR_PP_LIST_FIRST_N_O_D data
#else
#   define QOR_PP_LIST_FIRST_N_O( d, data )		QOR_PP_LIST_FIRST_N_O_D( QOR_PP_TUPLE_ELEM( 3, 0, data ), QOR_PP_TUPLE_ELEM( 3, 1, data ), QOR_PP_TUPLE_ELEM( 3, 2, data ) )
#endif

#define QOR_PP_LIST_FIRST_N_O_D( c, l, nl )		( QOR_PP_DEC( c ), QOR_PP_LIST_REST( l ), ( QOR_PP_LIST_FIRST( l ), nl ) )

//QOR_PP_LIST_FIRST_N_D

#if ~QOR_PP_CONFIG_FLAGS() & QOR_PP_CONFIG_EDG()
#   define QOR_PP_LIST_FIRST_N_D( d, count, list )		QOR_PP_LIST_REVERSE_D( d, QOR_PP_TUPLE_ELEM( 3, 2, QOR_PP_WHILE_ ## d( QOR_PP_LIST_FIRST_N_P, QOR_PP_LIST_FIRST_N_O, ( count, list, QOR_PP_NIL ) ) ) )
#else//EDG
#   define QOR_PP_LIST_FIRST_N_D( d, count, list )		QOR_PP_LIST_FIRST_N_D_I( d, count, list )
#   define QOR_PP_LIST_FIRST_N_D_I( d, count, list )	QOR_PP_LIST_REVERSE_D( d, QOR_PP_TUPLE_ELEM( 3, 2, QOR_PP_WHILE_ ## d( QOR_PP_LIST_FIRST_N_P, QOR_PP_LIST_FIRST_N_O, ( count, list, QOR_PP_NIL ) ) ) )
#endif

#endif//QOR_PREPROCESSOR_LIST_FIRST_N_H_1
