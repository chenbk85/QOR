#include "MammutQOR/Models/DOM/MTDOMEntityReference.h"

//------------------------------------------------------------------------------
namespace nsMammut
{
	//------------------------------------------------------------------------------
	CEntityReference::CEntityReference( CModel* pContainer ) : CDOMNode( pContainer )
	{
	}

	//------------------------------------------------------------------------------
	CEntityReference::CEntityReference( const CEntityReference& src ) : CDOMNode( src )
	{
	}

	//--------------------------------------------------------------------------------
	//Construct from a generic PropertySet
	CEntityReference::CEntityReference( CPropertySet& src ) : CDOMNode( src )
	{
	}

	//------------------------------------------------------------------------------
	CEntityReference::~CEntityReference()
	{
	}

	//------------------------------------------------------------------------------
	CEntityReference& CEntityReference::operator = ( const CEntityReference& src )
	{
		if( &src != this )
		{
			CDOMNode::operator=( src );
		}
		return *this;
	}

	//--------------------------------------------------------------------------------
	//assign from a generic property set
	CEntityReference& CEntityReference::operator = ( CPropertySet& src )
	{
		if( &src != this )
		{
			CDOMNode::operator=( src );
		}
		return *this;
	}

	//------------------------------------------------------------------------------
	bool CEntityReference::operator == ( const CEntityReference& src )
	{
		return &( const_cast< CEntityReference& >( src ) ) == this ? true : false;
	}

	//------------------------------------------------------------------------------
	bool CEntityReference::ValidChildType( CMUint16 NodeType )
	{
		return ( NodeType == ELEMENT_NODE || NodeType == TEXT_NODE || NodeType == COMMENT_NODE || NodeType == PROCESSING_INSTRUCTION_NODE || NodeType == CDATA_SECTION_NODE || NodeType == ENTITY_REFERENCE_NODE );
	}
}//nsMammut
