#include "MammutQOR/Models/DOM/MTDOMCharacterData.h"

//------------------------------------------------------------------------------
namespace nsMammut
{
	//------------------------------------------------------------------------------
	CComment::CComment(CModel* pContainer) : CCharacterData(pContainer)
	{
		m_NodeType.Set( CMUint16( COMMENT_NODE ) );
	}

	//------------------------------------------------------------------------------
	CComment::CComment(const CComment& src) : CCharacterData(src)
	{
	}

	//--------------------------------------------------------------------------------
	//Construct from a generic PropertySet
	CComment::CComment(CPropertySet& src) : CCharacterData(src)
	{
		m_NodeType.Set( CMUint16( COMMENT_NODE ) );
	}

	//------------------------------------------------------------------------------
	CComment::~CComment()
	{
	}

	//------------------------------------------------------------------------------
	CComment& CComment::operator = (const CComment& src)
	{
		if (&src != this)
		{
			CCharacterData::operator=(src);
		}
		return *this;
	}

	//--------------------------------------------------------------------------------
	//assign from a generic property set
	CComment& CComment::operator = (CPropertySet& src)
	{
		if (&src != this)
		{
			CCharacterData::operator=(src);
		}
		return *this;
	}

	//------------------------------------------------------------------------------
	bool CComment::operator == (const CComment& src)
	{
		return &(const_cast< CComment& >(src)) == this ? true : false;
	}

}//nsMammut


