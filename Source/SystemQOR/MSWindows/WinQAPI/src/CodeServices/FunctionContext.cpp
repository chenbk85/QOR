#include "WinQAPI/Kernel32.h"
#include "../SystemQOR/MSWindows/WinQAPI/include/FunctionContext.h"
#include "CodeQOR/Modules/ProcessBase.h"

//Windows specific extended function context implementation. Adds a Windows error code cache

//--------------------------------------------------------------------------------
namespace nsWinQAPI
{
	//--------------------------------------------------------------------------------
	///Note: These should only ever be created on the stack
	CFunctionContext::CFunctionContext( const char* szFuncName, const char* szFile, unsigned int uiLine ) : nsCodeQOR::CFunctionContextBase( szFuncName, szFile, uiLine )
	, m_dwLastOSError( CKernel32::GetLastError() )
	{
	}

	//--------------------------------------------------------------------------------
	CFunctionContext::CFunctionContext( nsCodeQOR::CObjectContextBase ObjectContext, const char* szFuncName, const char* szFile, unsigned int uiLine ) : nsCodeQOR::CFunctionContextBase( ObjectContext, szFuncName, szFile, uiLine )
	, m_dwLastOSError( CKernel32::GetLastError() )
	{
	}

	//--------------------------------------------------------------------------------
	CFunctionContext::~CFunctionContext()
	{
	}

	//--------------------------------------------------------------------------------
	unsigned long CFunctionContext::LastOSError()
	{
		return m_dwLastOSError;
	}

}//nsWinQAPI
