#include "SystemQOR.h"

#if		(QOR_SYS_OS == QOR_SYS_MSW)

#include "SystemQOR/MSWindows/MSW_pthread.h"
#include "../Source/SystemQOR/MSWindows/Winpthreads/details/misc.h"
#include <sys/timeb.h>

//--------------------------------------------------------------------------------
namespace nsWin32
{
	//--------------------------------------------------------------------------------
	unsigned long long Cpthread::time_in_ms(void)
	{
		_timeb tb;

		//TODO:_ftime(&tb);
		return (unsigned long long)tb.time * 1000ULL + (unsigned long long) tb.millitm;
	}

	//--------------------------------------------------------------------------------
	unsigned long long Cpthread::time_in_ms_from_timespec( const timespec* ts )
	{
		unsigned long long t = (unsigned long long) ts->tv_sec * 1000LL;
		t += (unsigned long long) (ts->tv_nsec / 1000000);

		return t;
	}

	//--------------------------------------------------------------------------------
	unsigned long long Cpthread::rel_time_in_ms( const timespec* ts )
	{
		unsigned long long t1 = time_in_ms_from_timespec(ts);
		unsigned long long t2 = time_in_ms();

		// Prevent underflow
		if (t1 < t2) 
		{
			return 0;
		}
		return t1 - t2;
	}

}//nsWin32

#endif//(QOR_SYS_OS == QOR_SYS_MSW)
