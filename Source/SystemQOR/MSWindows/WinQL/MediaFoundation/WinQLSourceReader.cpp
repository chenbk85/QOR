#include "WinQL/CodeServices/WinQLPolicy.h"
#include "WinQL/Application/Threading/WinQLCriticalSection.h"
#include "WinQL/MediaFoundation/SourceReader.h"
#include "WinQAPI/MedFoundAPI.h"

//--------------------------------------------------------------------------------
namespace nsWin32
{
	//--------------------------------------------------------------------------------
	namespace nsMediaFoundation
	{
		__QOR_IMPLEMENT_OCLASS_LUID( CSourceReader );

		//--------------------------------------------------------------------------------
		//Create a source reader from a byte stream
		CSourceReader::CSourceReader( IMFByteStream* pByteStream, IMFAttributes* pAttributes )
		{
			_WINQ_FCONTEXT( "CSourceReader::CSourceReader" );
			m_ulStatus = m_ReadWriteLibrary.MFCreateSourceReaderFromByteStream(
				reinterpret_cast< ::IMFByteStream* >( pByteStream ),
				reinterpret_cast< ::IMFAttributes* >( pAttributes ),
				reinterpret_cast< ::IMFSourceReader** >( &m_p ) );
		}

		//--------------------------------------------------------------------------------
		//Create a source reader from a media source
		CSourceReader::CSourceReader( IMFMediaSource* pMediaSource, IMFAttributes* pAttributes )
		{
			_WINQ_FCONTEXT( "CSourceReader::CSourceReader" );
			m_ulStatus = m_ReadWriteLibrary.MFCreateSourceReaderFromMediaSource(
				reinterpret_cast< ::IMFMediaSource* >( pMediaSource ),
				reinterpret_cast< ::IMFAttributes* >( pAttributes ),
				reinterpret_cast< ::IMFSourceReader** >( &m_p ) );
		}

		//--------------------------------------------------------------------------------
		//Create a source reader from a URL
		CSourceReader::CSourceReader( const wchar_t* pwszURL, IMFAttributes* pAttributes )
		{
			_WINQ_FCONTEXT( "CSourceReader::CSourceReader" );
			m_ulStatus = m_ReadWriteLibrary.MFCreateSourceReaderFromURL( pwszURL, 
				reinterpret_cast< ::IMFAttributes* >( pAttributes ),
				reinterpret_cast< ::IMFSourceReader** >( &m_p ) );
		}

		//--------------------------------------------------------------------------------
		CSourceReader::~CSourceReader()
		{
			_WINQ_FCONTEXT( "CSourceReader::~CSourceReader" );
		}

	}//nsMediaFoundation

}//nsWin32
