	//--------------------------------------------------------------------------------
	Cmp_unsigned_long_long Climits::ULongLongMax()
	{
		__QCS_FCONTEXT( "Climts::ULongLongMax" );
		static const Cmp_unsigned_long_long Max = ~( Cmp_unsigned_long_long( 0x0 ) );
		return Max;
	}

	//--------------------------------------------------------------------------------
	Cmp_long_long Climits::LongLongMax()
	{
		__QCS_FCONTEXT( "Climts::LongLongMax" );
		static const Cmp_long_long Max = ( ( Cmp_unsigned_long_long( ~0x0 ) ) >> 1 );
		return Max;
	}

	//--------------------------------------------------------------------------------
	Cmp_long_long Climits::LongLongMin()
	{
		__QCS_FCONTEXT( "Climts::LongLongMin" );
		static const Cmp_long_long Max = ~( ( Cmp_unsigned_long_long( ~0x0 ) ) >> 1 );
		return Max;
	}

